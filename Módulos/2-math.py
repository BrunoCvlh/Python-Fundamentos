import math

# Acessando PI
print(math.pi)
print(f"{math.pi:.2f}")

# Acessando o número de Euler
print(math.e)
print(f"{math.e:.2f}")


# Arredondamento
num = 10.4
print(math.ceil(num))
print(math.floor(num))

# Fatorial de um número
num2 = 5
print(math.factorial(num2))

# Potência de um número
print(math.pow(5, 5))

# Raiz quadrada de um número
print(math.sqrt(169))

# MDC - Máximo Divisor Comum
mdc = math.gdc(20, 100)
print(mdc)

# Logaritmo
print(math.log(10))